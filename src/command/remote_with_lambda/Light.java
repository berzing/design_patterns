package command.remote_with_lambda;


/**
 * 电灯
 * 厂商类  控制特定的家电自动化装置
 */
public class Light {

    String location = "";

    public Light(String location) {
        this.location = location;
    }

    public void on() {
        System.out.println(location + " light is on");
    }

    public void off() {
        System.out.println(location + " light is off");
    }
}
